using UnityEngine;

public class Connections
{
    public int Xpos;
    public int Xneg;
    public int Ypos;
    public int Yneg;
    public int Zpos;
    public int Zneg;

    public Connections(int Xpos, int Xneg, int Ypos, int Yneg, int Zpos, int Zneg)
    {
        this.Xpos = Xpos;
        this.Xneg = Xneg;
        this.Ypos = Ypos;
        this.Yneg = Yneg;
        this.Zpos = Zpos;
        this.Zneg = Zneg;
        ;
    }
}
