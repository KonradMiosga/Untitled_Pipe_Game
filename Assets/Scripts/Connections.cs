using UnityEngine;

public class Connections
{
    public int posX;
    public int negX;
    public int posY;
    public int negY;
    public int posZ;
    public int negZ;

    public Connections(int posX, int negX, int posY, int negY, int posZ, int negZ)
    {
        this.posX = posX;
        this.negX = negX;
        this.posY = posY;
        this.negY = negY;
        this.posZ = posZ;
        this.negZ = negZ;
    }
}
